package com.er.OOP.reflection;

public class OtherClass {

    int i;
    String s;

    public OtherClass() {
    }

    public OtherClass(int i, String s) {
        this.i = i;
        this.s = s;
    }

    public int getI() {
        return i;
    }

    public String getS() {
        return s;
    }
}
